# Basic Control and For Loops

# Problem 1: Print "Hello, World!" three times using a for loop
for i in range(3):
    print("Hello, World!")

# Problem 2: Print the numbers 0 to 9 using a for loop
for i in range(10):
    print(i)

# Problem 3: Use a for loop to calculate the sum of the first 10 integers (0 + 1 + 2 + ... + 9)
sum = 0
for i in range(10):
    sum += i
print(sum)

# Problem 4: Use a while loop to print the numbers from 1 to 5
i = 1
while i <= 5:
    print(i)
    i += 1
